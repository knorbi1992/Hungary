        <h4>Hydrography of Hungary</h4>
        <p>Hungary's hydrography is basically determined by the fact that it lies in the middle of the Carpathian Basin , surrounded by the semicircle of the Carpathians . There is no free-flowing area in the country, all surface water gravitates towards the southern center, and from there it reaches the Black Sea via the Danube and the Vaskapu Strait . The entire area belongs to the Danube catchment area .The annual water balance shows a surplus, approximately 100 billion m³ of water leaves the country towards the south. Only 10% of this comes from precipitation , the rest is brought by rivers from the surrounding areas .</p>
