        <h2>Parliament</h2>
        <div class="content-wrapper">
        <p>The layout of the Parliament is completely symmetrical, as the building was built for a bicameral parliament in 1896. The upper house was located in the northern wing, and the meeting room of the lower house was located in the southern wing. The two meeting rooms are mirror images of each other. Today, the lower house hall is used for parliamentary meetings, the upper house hall is open to tourists, and faction meetings, conferences, and receptions are held there. It is interesting that the floor carpet in the lounge in the lower house is red, but in the upper house, it is blue, since the members of the upper house were traditionally nobles, who were called blue-blooded.</p>
