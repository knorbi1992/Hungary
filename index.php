        <div class="content-wrapper">
            <p>
                99% of the people living in Hungary speak Hungarian as a first language. Hungarian is a Uralic language distantly related to Finnish and Estonian. English and German are the most widely spoken foreign languages, while several recognized minority languages include Croatian, German, Romanian, Romani, Serbian, Slovak, Slovenian, and Ukrainian. As for ethnic diversity, in 2011, 83% of the population was Hungarian, 3% Romani, 1% German, but almost 15% did not declare their ethnicity.
