        <h2>Culture and Heritage</h2>
        <p>
            Hungary is home to fascinating architectural and archaeological treasures – the largest synagogue and the largest medicinal bath in Europe, the second-largest Baroque castle in the world (Gödöllő), the third-largest church in Europe (Esztergom), the largest Early Christian Necropolis outside Italy (Pécs), and several Roman and medieval sites.
        </p>
        <p>
            Hungary has a thriving cultural scene with theater, ballet, and musical performances in most of its major cities and towns. The film industry has also reached international success. The Nobel Prize in Literature was awarded to Hungarian author Imre Kertész in 2002.
        </p>

        <h2>Cuisine</h2>
        <p>
            Hungarian cuisine is characterized by dishes based on meats, seasonal vegetables and fruits, dairy products, and spices such as pepper and paprika.
